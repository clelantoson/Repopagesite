	<h2>Comment obtenir son indemnisation ?</h2>
	<p>La prise de contact avec la compagnie n'est généralement <span class="font-bold">pas facile</span> : nombreuses heures d'attente au téléphone, difficulté à trouver les pages de réclamation, longueur à répondre, etc ...</p>
	<p>Il est préférable d'envoyer directement les éléments de droit du <span class="font-bold">règlement 261-2004</span> et de faire savoir aux compagnies que vous connaissez ce règlement et leur obligation à vous indemniser.</p>
	<p>Il est très fréquent que les compagnies proposent des bons d'achats pour de prochains vols. Sachez que rien ne vous oblige à accepter le bon d'achat, vous pouvez demander une indemnisation en numéraire.</p>
	<p>Il est aussi très fréquent que les montants proposés par les compagnies soient <span class="font-bold">très en dessous</span> des montants d'indemnisation auxquels vous avez droit. Attention donc à bien calculer le montant de votre indemnisation sur votre vol annulé.</p>
	<p>Enfin, certaines compagnies peu scrupuleuses évoqueront des circonstances exceptionnelles afin de se dédouaner de la responsabilité du retard de votre vol. Une bonne connaissance de la jurisprudence est alors nécessaire pour contrer lorsque c'est possible les arguments des compagnies dont le vol est annulé.</p>

	<p><strong>Pour simplifier vos démarches et gagner du temps</strong>: vous pouvez faire appel à RetardVol. Notre équipe de juristes et avocats experts en droit aérien s'occuppent des procédures pour vous et vous obtiendrez l'indemnité maximale à laquelle vous avez le droit. Si jamais les compagnies aériennes ne coopèrent pas nous les assigons en justice gratuitement pour vous avec nos avocats.</p>
