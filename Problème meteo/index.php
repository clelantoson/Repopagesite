			<p>L’objectif de l’Union européenne est de garantir une protection des passagers dans le domaine du transport aérien. Cette protection a été concrétisée avec l’adoption du réglement N°261/2004 du 11 février 2004 qui instaure des règles communes relatives à l’indemnisation et à l’assistance des passagers en cas d'annulation de vol.</p>


			<h2>Montant de l'indemnité pour un vol annulé </h2>

			<p>Le règlement Européen 261-2004 prévoit une indemnisation financière pour les passagers ayant subi un vol annulé. Cette indemnité peut être cumulable avec un éventuel remboursement du billet par la compagnie. </p>


			<div class="mep-lu-il">
				<ul>

					<li><i class="fa fa-check" aria-hidden="true" style="color:green"></i><strong> 250 €</strong> par passager, pour les vols annulés dont la distance est de moins de 1.500 km</li>

					<li><i class="fa fa-check" aria-hidden="true" style="color:green"></i><strong> 400 €</strong> par passager, pour les vols annulés dont la distance est entre 1.500km et 3.500km</li>

					<li><i class="fa fa-check" aria-hidden="true" style="color:green"></i><strong> 400 €</strong> par passager, pour les vols de plus de 3.500km mais qui restent intra-communautaires (de métropole vers les DOM-TOM par exemple)</li>


					<li><i class="fa fa-check" aria-hidden="true" style="color:green"></i><strong> 600 €</strong> par passager, pour les vols annulés dont la distance est de plus de 3.500km et non intra-communautaires (de Paris à New-York par exemple)</li>

				</ul>
			</div>


